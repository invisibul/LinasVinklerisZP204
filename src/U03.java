
public class U03 {

	public static void main(String[] args) {
		int a = -100, b = -199;
		System.out.println("ciklas for: ");
		for (int i = a; i >= b; i = i-3){
			System.out.print(i + " ");
		}
		System.out.println();
		System.out.println("ciklas while: ");
		while (a >= b) {
			System.out.print(a + " ");	
			 a = a-3;
		}
			
			
	}

}
